package app.shop.presentprototype;

public class PresentPrototype {
	public int id;
	public String title;	
	public int price;
	public int showed;
	public String url;
	
	public PresentPrototype(int id, String title, int price, int showed, String url){
		this.id = id;
		this.title = title;		
		this.price = price;
		this.showed = showed;
		this.url = url;
	}
}
